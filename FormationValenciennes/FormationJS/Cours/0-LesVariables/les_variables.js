// Déclarer une variables en js (ES5)
var prenom;

// Affecter une valeur à la variable
prenom="Anthony";

// Afficher la valeur de la variable
console.log(prenom);

// Les types de variables
// On ne définit pas de type à la déclaration 
// mais il y a quand même un type pour la variable
// (Variables Auto typé)
console.log(typeof(prenom));


// Declarer et affecter une valeur à une variable en une instruction
var age=32;
console.log(typeof(age));

// Concaténation de chaines (se fait par l'intermédiaire de l'opérateur + )
console.log("Bonjour je m'appel "+prenom+" et j'ai "+age+" ans");
